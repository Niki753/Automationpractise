package day5.basics;

public class swap2 {

	public static void main(String[] args) {
     float a = 10.5f, b= 20.5f;
     System.out.println("value of a = " +a);
     System.out.println("value of b = " +b);
     float z = a; // z= 10.5f
     a=b; //a = 20.5f
     b = z; //b= 10.5f
     System.out.println("value of a = " +a);
     System.out.println("value of b = " +b);
     
	}

}

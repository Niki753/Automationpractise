package randompractise1;

public class Cifelseevenorodd1 {

	static void evenodd(int num) {
		// int num
		if (num % 2 == 0) {
			System.out.println("num is even");
		} else {
			System.out.println("num is odd");

		}
	}

	public static void main(String[] args) {

		evenodd(10);
		evenodd(11);
		
	}

}

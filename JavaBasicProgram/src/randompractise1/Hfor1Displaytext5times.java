package randompractise1;

public class Hfor1Displaytext5times {

	static void display(char c1) {

		for (int i = 1; i < 6; i++) {
			System.out.println(c1);
		}
	}

	public static void main(String[] args) {

		for (int i = 1; i < 6; i++) {
			System.out.println(" Good Morning ");
		}
		display('a');

		for (int i = 1; i < 6; i++) {
			System.out.println(i);
		}
	}

}
